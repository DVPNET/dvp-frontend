<template>
  <div class="trix-container">
    <trix-toolbar v-stickto id="my_toolbar"></trix-toolbar>
    <trix-editor
      toolbar="my_toolbar"
      :input="inputId || randomId"
      class="trix-content"
      ref="trix"
      @trix-change="update"
      @trix-attachment-add="emitAttachmentAdd"
      @trix-attachment-remove="emitAttachmentRemove">
    </trix-editor>
    <input
      type="hidden"
      name="content"
      :id="inputId || randomId"
      :value.prop="initContent">
  </div>
</template>

<script>
import Vue from 'vue'
import 'trix'
import 'trix/dist/trix.css'
import SaveEditorState from '../mixins/SaveEditorState.js'
import EmitAttachmentAdd from '../mixins/EmitAttachmentAdd.js'
import EmitAttachmentRemove from '../mixins/EmitAttachmentRemove.js'

Vue.config.ignoredElements = ['trix-editor','trix-toolbar']

export default {
  name: 'VueTrix',
  mixins: [
    SaveEditorState('VueTrixEditor'),
    EmitAttachmentAdd('VueTrixEditor'),
    EmitAttachmentRemove('VueTrixEditor')
  ],
  model: {
    prop: 'initContent',
    event: 'update'
  },
  props: {
    inputId: {
      type: String,
      required: false,
      default () {
        return ''
      }
    },
    initContent: {
      type: String,
      required: false,
      default () {
        return ''
      }
    }
  },
  methods: {
    update (event) {
    	var obj=event.srcElement || event.target
      this.$emit('update', obj.innerHTML)
    }
  },
  computed: {
    randomId () {
      let text = ''
      let possible = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789'

      for (let i = 0; i < 10; i++) {
        text += possible.charAt(Math.floor(Math.random() * possible.length))
      }
      return text
    }
  },
  components:{
  }
}
</script>

<style lang="scss" scoped>
.trix-container {
  max-width: 100%;
  height: auto;
}
</style>
